    private void tbl_khMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_khMouseClicked
        i = tbl_kh.getSelectedRow();
        txt_makh.setText(tbl_kh.getValueAt(i, 1).toString());
        txt_hoten.setText(tbl_kh.getValueAt(i, 2).toString());
        if (tbl_kh.getValueAt(i, 3).toString().equalsIgnoreCase("Nam")) {
            rdo_Nam.setSelected(true);
        } else {
            Rdo_Nữ.setSelected(true);
        }
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String) mol.getValueAt(i, 4));
            date_ngaysinh.setDate(date);

        } catch (Exception e) {
        }
        txt_diachi.setText(tbl_kh.getValueAt(i, 5).toString());
        txt_sdt.setText(tbl_kh.getValueAt(i, 6).toString());
        txt_email.setText(tbl_kh.getValueAt(i, 7).toString());
    }//GEN-LAST:event_tbl_khMouseClicked

