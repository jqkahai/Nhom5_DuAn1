Model_KhachHang readForm() throws ParseException {
        String makh;
        String hoten;
        Boolean gt;
        String ngaysinh;
        String diachi;
        int sdt;
        String email;
        makh = txt_makh.getText().trim();
        if (makh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập Mã Khách Hàng!");
            txt_makh.requestFocus();
            return null;
        }
        hoten = txt_hoten.getText().trim();
        if (hoten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tên Khách Hàng!");
            txt_hoten.requestFocus();
            return null;
        }
        if (rdo_Nam.isSelected()) {
            gt = true;
        } else {
            gt = false;
        }
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        ngaysinh = dateFormat.format(date_ngaysinh.getDate());
        diachi = txt_diachi.getText().trim();
        if (diachi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập Địa chỉ!");
            txt_diachi.requestFocus();
            return null;
        }
        sdt = Integer.parseInt(txt_sdt.getText().toString());
        email = txt_email.getText().trim();
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập Email!");
            txt_email.requestFocus();
            return null;
        }
        System.out.println(ngaysinh);
        return new Model_KhachHang(makh, hoten, gt, ngaysinh, diachi, sdt, email);
    }

